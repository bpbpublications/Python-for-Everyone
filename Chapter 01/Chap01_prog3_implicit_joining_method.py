# Using curly braces
days = {'Mon','Tue','Wed',
        'Thu','Fri','Sat',
        'Sun'}
print(days)

# Using square brackets
days = ['Mon','Tue','Wed',
        'Thu','Fri','Sat',
        'Sun']
print(days) 

#Using parenthesis
days = ('Mon','Tue','Wed',
        'Thu','Fri','Sat',
        'Sun')
print(days)

# We can write comment after the line.
days = {'Mon','Tue','Wed', # Mon - Wed
        'Thu','Fri','Sat', # Thu - Sat
        'Sun'} # sun
print(days)

# Blank lines can be inserted.
days = { #Blank
        'Mon','Tue','Wed', # Mon - Wed
        'Thu','Fri','Sat', # Thu - Sat
        'Sun'} # sun
print(days)


